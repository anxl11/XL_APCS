public class SimpleCounterButAForLoop{
    public static void main(String []args){
        // for loop for each interval
        for(int i=0;i<=300;i++){
            System.out.println(i);
        }
        for(int i=300;i>=0;i--){
            System.out.println(i);
        }
        for(int i=4798;i<=5324;i++){
            System.out.println(i);
        }
        for(int i=5324;i<=4798;i--){
            System.out.println(i);
        }
    }
}